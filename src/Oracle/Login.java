    private void btn_SignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SignUpActionPerformed
        // TODO add your handling code here:
        String username = txt_Username.getText().trim();
        String password = new String(txt_Password.getPassword());
        conn.connectToOracle("Create_user", "123");
        String statement = "{call " + publicProcedure.Signup + "(?,?)}";
        try{
            conn.executingProcedure(statement, new String[]{username,password});
            JOptionPane.showMessageDialog(this, "Registered");
        } catch(Error err){
            JOptionPane.showMessageDialog(this, "Username existed");
        }
        conn.disconnectOracle();
    }//GEN-LAST:event_btn_SignUpActionPerformed

