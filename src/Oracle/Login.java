    private void btn_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_LoginActionPerformed
        // TODO add your handling code here:
        String username = txt_Username.getText().trim();
        String password = new String(txt_Password.getPassword());
        try{
            conn.connectToOracle(username, password);
            Main mainForm = new Main();
            login = this;
            login.setVisible(false);
            mainForm.setVisible(true);
            mainForm.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosed(java.awt.event.WindowEvent windowEvent) {
                setVisible(true); // Hiển thị lại Form 1
                txt_Username.setText("");
                txt_Password.setText("");
            }
        });
        } catch(Error | Exception err){
            JOptionPane.showMessageDialog(this, "Username or password doesn't exist!");
        }
    }//GEN-LAST:event_btn_LoginActionPerformed

